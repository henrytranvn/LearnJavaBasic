package lesson1;

public class LessonOne {
    public static void main(String[] args) {
        int a = 15;
        int b = 3;
        int intResultPlus = a + b;
        int ResultMinus = a - b;
        int intResultMulti = a * b;
        int intResultDivision = a / b;
        System.out.println(a + " + " + b + " = " + intResultPlus);
        System.out.println(a + " - " + b + " = " + ResultMinus);
        System.out.println(a + " * " + b + " = " + intResultMulti);
        System.out.println(a + " / " + b + " = " + intResultDivision);
    }
}
